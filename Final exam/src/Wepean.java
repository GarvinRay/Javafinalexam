public class Wepean {
    private String name;
    private int id;
    private int damage;
    public Wepean(String name, int id, int damage) {
        this.name = name;
        this.id = id;
        this.damage = damage;
    }
    public String getName() {
        return name;
    }
    public int  getId() {
        return id;
    }
    public int getDamage() {
        return damage;
    }
}
